function multiplicacao(x: number, y: number): number {
    return x * y;
}

// Exemplo de uso
const resultadoMultiplicacao: number = multiplicacao(5, 3);
console.log(resultadoMultiplicacao); // Saída esperada: 15


function saudacao(nome: string): string {
    return "Olá " + nome;
}

// Exemplo de uso
const mensagemSaudacao: string = saudacao("Alice");
console.log(mensagemSaudacao); // Saída esperada: Olá Alice
